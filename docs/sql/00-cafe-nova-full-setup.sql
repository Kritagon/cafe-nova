create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nombre text not null,
  rol text not null default 'admin',
  activo boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),

  constraint profiles_rol_check
    check (rol in ('admin'))
);

create table if not exists public.categorias (
  id bigint generated by default as identity primary key,
  nombre text not null,
  descripcion text,
  activo boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),

  constraint categorias_nombre_unique unique (nombre)
);

create table if not exists public.productos (
  id bigint generated by default as identity primary key,
  categoria_id bigint not null references public.categorias(id),
  nombre text not null,
  descripcion text,
  precio numeric(10,2) not null,
  presentacion text not null,
  tipo_cafe text not null,
  imagen_url text,
  activo boolean not null default true,
  destacado boolean not null default false,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),

  constraint productos_precio_check
    check (precio >= 0),

  constraint productos_tipo_cafe_check
    check (tipo_cafe in ('molido', 'grano', 'otro'))
);

create table if not exists public.pedidos (
  id bigint generated by default as identity primary key,
  codigo_pedido text not null unique,
  nombre_cliente text not null,
  telefono text not null,
  correo text,
  direccion text,
  comentarios text,
  estado text not null default 'pendiente',
  total_estimado numeric(10,2) not null default 0,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),

  constraint pedidos_estado_check
    check (estado in ('pendiente', 'confirmado', 'en_preparacion', 'entregado', 'cancelado')),

  constraint pedidos_total_estimado_check
    check (total_estimado >= 0)
);

create table if not exists public.pedido_detalle (
  id bigint generated by default as identity primary key,
  pedido_id bigint not null references public.pedidos(id) on delete cascade,
  producto_id bigint not null references public.productos(id),
  nombre_producto text not null,
  precio_unitario numeric(10,2) not null,
  cantidad integer not null,
  subtotal numeric(10,2) not null,
  created_at timestamp with time zone not null default now(),

  constraint pedido_detalle_precio_unitario_check
    check (precio_unitario >= 0),

  constraint pedido_detalle_cantidad_check
    check (cantidad > 0),

  constraint pedido_detalle_subtotal_check
    check (subtotal >= 0)
);

-- Campo agregado posteriormente para notas internas del administrador
alter table public.pedidos
add column if not exists notas_admin text;

